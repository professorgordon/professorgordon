                  <h4>Overview</h4>
                </div>
                <div class="w3-container">
                  <p>
                    Professional skills are competencies that you develop over time in school, in your work and in your daily life. These skills surround your technical skills and help make you a well-rounded individual. In school you might major in Mathematics, Engineering, Computer Science, etc., all of which focus on the technical skills involved in those subjects. We often assume that’s all you need, if you can write computer programming code then if follows that you will be able to get a job later as a computer programmer. However, it is your general education classes in English, Natural Sciences, Social Sciences, Humanities, Diversity, etc. that provide you with the focus and practice of the professional skills that are critical to you finding, and maybe more importantly, keeping and advancing, in a job, any job, later.
                  </p>
                  <p>
                    You have already developed various levels competence in these skills, both directly and indirectly, simply by attending primary and secondary education, interacting with others throughout your life and through any work experience you may have. As you study technical topics like computer science, engineering, mathematics, etc., it is very easy to place 100% of your focus in technical classes on the technical skills and assume you already have proficiency in the reading, writing, analytical thinking, research, constructive criticism, time management, interpersonal skills, etc. that surround those technical subjects. It is that assumption in technical classes that is a disservice to your education. To ignore the development of your professional skills in parallel with technical skills, by assuming you will gain everything you need through general education classes, creates a hole in your education which translates to a weakness that you will likely have to fix on your own later. The time to work on this is during your education rather than later when you are working which it much more difficult.
                  </p>
                  <p>
                    During my 30+ years in the IT industry I have seen many cases of people who are highly proficient in their technical skills but who suffer weaknesses in their ability to articulate problems in writing or verbally. This problem often shows itself initially during their job interviews when when interviewers are trying to assess not only if the job candidate has the requisite technical skills but can the person interact with non-technical people in the form of being able to explain ideas, ask questions and adapt to those who they are interacting with. Once in a job, those competencies actually increase in importance. As a person’s career progresses those skills become more and more important. Yes it is expected that your technical skills will continue to increase over time, but your ability to work with non-technical people also increases requiring you to be even more able to demonstrate your professional skills.
                  </p>
                  <p>
                    So, the bottom line is, it will most likely be your professional skills that will get you job interviews and ultimately the job offer. How? In order to get a job, you will have to write a resume and cover letter, you will have to research the companies you are applying to, you will have to dress appropriately for your interviews, you will have to communicate with non-technical people (HR representatives, receptionists, etc.) in order to get the interview. Once you’re in the interview, you will have to present yourself professionally and demonstrate your ability to communicate verbally and likely you’ll be asked questions that demonstrate your ability to solve problems. After your interviews you should write a thank you email that you send to those who you interviewed with. Then, as you progress in your career, you will find that these skills become more and more important over time. You will use them to better interact with co-workers, customers, clients, employers, managers, and you will use them to continue to improves your own technical and professional skills.
                  </p>
                  <p>
                    How do you practice these skills in school? First, it is important to get an idea of what skills we’re talking about, so see the list below. And second, actively look for ways to consciously practice good approaches to these skills.
                  </p>
                  <p>
                    <h4>Skills</h4>
                  </p>
                  <p>
                    Professional skills are often referred to as “soft skills,” which is a common term for them. Many people consider the term “soft skills” a weak term which diminishes the importance of these critical skills and encourages technical educators to place minimal or no focus on them. However, when searching for information on these types of skills, often using “soft skills” will result in a large amount of information.
                  </p>
                  <p>
                    Here is an alphabetical list of some (there are others) of the professional skills I recommend that you begin to put effort in to improving for yourself and your future.
                  </p>
                  <p>
                    <ul>
                      <li>Adaptability</li>
                      <li>Analytical Thinking</li>
                      <li>Detail Oriented</li>
                      <li>Communicating</li>
                      <li>Conflict Resolution</li>
                      <li>Constructive Criticism</li>
                      <li>Creativity</li>
                      <li>Dependability</li>
                      <li>Ethics</li>
                      <li>Flexibility</li>
                      <li>Honesty</li>
                      <li>Integrity</li>
                      <li>Interpersonal</li>
                      <li>Leadership</li>
                      <li>Listening</li>
                      <li>Patience</li>
                      <li>Problem-Solving</li>
                      <li>Reaching Deadlines</li>
                      <li>Reading</li>
                      <li>Research</li>
                      <li>Speaking (to individuals and groups)</li>
                      <li>Teamwork</li>
                      <li>Time-Management</li>
                      <li>Writing</li>
                    </ul>
                  </p>
                  <p>
                    As you progress in your education you will work on many types of assignments, projects and challenges. In all cases, you can choose to place some conscious emphasis on professional skills in which you will explore what these skills are and how to apply them to your school work, your jobs and your life.
                  </p>
