                <div class="w3-container">
                  <h4>Linux Books I Like</h4>
                </div>
                <div class="w3-container">
                  <p>
                    <ul>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/0134277554/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=0134277554&linkCode=as2&tag=professorgord-20&linkId=c5e2b2693a6054318200a004c60dbf21">Unix and Linux System Administration Handbook</a>, 5th Edition, 2017, by Evi Nemeth, et. al. This book is not only a #1 best seller but also a de facto standard in the industry. I have worked out of nearly every edition of the Nemeth book editions over the years. If you have any intention of working in system administration in the Unix/Linux world and can only buy one book, this is the one.
                      </li>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/1593279523/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=1593279523&linkCode=as2&tag=professorgord-20&linkId=44108adac3f6b858ccd44a1b9967cbfe">The Linux Command Line</a>, 2nd Edition, 2019, by William Shotts. The Shotts books is an exceptional text when you are focused on learning the Linux command line, which is a set of critical skills to have in you are going to work with Linux at all.
                      </li>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/B01HUOX0GY/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=B01HUOX0GY&linkCode=as2&tag=professorgord-20&linkId=348303fd67c33d0ab8d6f9f40b4769f2">Introducing Linux Distros</a>, 1st Edition, 2016, by Jose Dieguez Castro. The unfortunate reality of of Linux distributions is the high rate of change that they go through over time. So, you could discount this book because it’s a few years old and the distros it highlights have changed, but, don’t overlook the fact that this book is great if you are just starting and need to understand what distros are and how critical they are to the Linux world.
                      </li>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/1593275676/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=1593275676&linkCode=as2&tag=professorgord-20&linkId=bf4edad411fa00ba761f236f10aaf52d">How Linux Works</a>, 2nd Edition, 2014, by Brian Ward. Also a bit of a dated book in the fast-paced technology world, but, it still contains details of how Linux really works that is hard to find elsewhere. Also, the <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/1718500408/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=1718500408&linkCode=as2&tag=professorgord-20&linkId=152fc44fb8dcfc501567c773a9c4ee2d">3rd Edition</a> of this book is due to come out in November 2020.
                      </li>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/1593276028/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=1593276028&linkCode=as2&tag=professorgord-20&linkId=7b87ed532fb8e1cf9718c46fba3adeaa">Wicked Cool Shell Scripts</a>, 2nd Edition, 2016, by Dave Taylor. This presents a great set of scripting ideas that are well worth studying and using as jumping off points into more advanced topics in scripting.
                      </li>
                      <li>
                        <a style="color:#0000ff;" target="_blank" href="https://www.amazon.com/gp/product/1593278551/ref=as_li_tl?ie=UTF8&camp=1789&creative=9325&creativeASIN=1593278551&linkCode=as2&tag=professorgord-20&linkId=bd8c18fffd1b04b3d7e947497004178b">Linux Basics for Hackers</a>, 2018, by OccupyTheWeb. The book title might throw some, but it’s not dark text just for “hackers,” it is a great overview of Linux, gets you to dip your toes into many cool topics in the operating system and doesn’t waste your time – straight to the point.
                      </li>
                    </ul>
                  </p>
