                  <h4>Overview</h4>
                </div>
                <div class="w3-container">
                  <p>
                    An early step in studying careers in IT is to get a basic idea of how businesses structure the teams that handle their technology needs. There is no single way in which companies do this, different companies set up their technology groups in different ways depending on their needs. Also, the type of business, the size of the business, whether they are located in a single building or dispersed over large geographic areas, whether they are national or international and many other factors also play a role in how they might structure their IT Departments. I will be using an example of a structure in this lesson that loosely mimics several organizations that I have been involved with over during my career. The example I will use for this discussion would be indicative of a mid to large sized company that has enough resources and needs to justify a set of IT teams.
                  </p>
                  <p>
                    As a student looking at the IT industry as a possible career, the first thing you should notice is that there are many paths to choose from. For example, you might have a strong interest in programming so it would be logical to focus only on careers in programming. The reality is that programming occurs in many different ways in many different areas of a company and throughout the industry. Similarly, other areas of IT like databases, networking, etc. often cross over lines in companies depending on the needs of different components of the business.
                  </p>
                  <p>
                    So, it is important to keep in mind while reading about the this example IT department, this example only covers a small percentage of the myriad of different environments you might find as you progress in the industry. In my career I have worked in very small companies, where I was the only technology staff member, in medium-sized companies with small teams of technology staff and in very large companies where there were hundreds of technology staff members. There are similarities and differences in all of these different types of environments.
                  </p>
                  <p>
                    As I mentioned previously, there are many factors involved in what you might find in different types of businesses. There are certainly differences between a bank and a company that creates video games, for example. The important thing to remember is that they are both (all) businesses. They are in business to make money. That is the leveling detail to keep in mind–all companies, regardless of their market, product or service, are in business to make a profit. Even non-profit organizations must have income in order to operate and pay their employees, even if that income is acquired largely from donations or government funding.
                  </p>
                  <p>
                    So, the example company I will be using in this lesson is, let’s say, a financial planning and lending company with 10 billion dollars of annual revenue, is national (that means, it has offices in many states in the United States), employs 5,000 people and has an IT department of 100 staff members. That group of 100 IT employees handle all of the company’s needs including software development, databases, web site development, data science, etc. In this example, we will assume that all IT positions are filled by full-time employees, except for a small number of consultants who work part-time for the company and are assigned to specific projects.
                  </p>
                  <p>
                    Here is a diagram that gives a high-level overview of what the IT Department for our sample company might look like:
                  </p>
                  <p>
                    <img src="images/ITDepartmentExample.jpg" class="w3-image" alt="IT Department">
                  </p>
                  <p>
                    The first thing to notice here is that software development is broken up into teams. In this example there is a User Interface Team, a Transaction Team and a Web Development Team. These 3 teams may not be the only teams in Software Development, there could be any number of others. We are just looking at a sample here. Now lets zoom in even further and look only at the User Interface Team:
                  </p>
                  <p>
                    <img src="images/ExampleSDTeam.jpg" class="w3-image" alt="SD Team">
                  </p>
                  <p>
                  Now we are at the level where we can see individual jobs. Since we zoomed into Software Development we see jobs related to that IT area, the other units in our original diagram above would have their own set of jobs.
                </p>
                <p>
                  The first thing to notice here is that most of these positions say “Developer.” In many companies developer and programmer are interchangeable terms. Technically though there is a distinction in which a developer is a programmer who has gained enough experience with the company, product or service that they are involved in the actual design of the software systems (while still also writing code for the system themselves). Programming tasks are often delegated to or shared with the other developers of various levels to write code to implement those designs.
                </p>
                <p>
                  In most companies, the first goal for a junior level employee is to learn enough about the company, product or service that they can move into the next higher level role, where they can participate in more involved aspects of the IT tasks.
                </p>
                <p>
                  Also, in this example, notice there’s a Junior Developer, this is often referred to as “entry level.” The idea of an entry level position exists in most IT Department units. In the example of Software Development, that Junior Developer works under a Senior Software Developer, who in turn works for the Manager of Software Development: User Interface Team. If you look at the previous diagram, each of the teams has a Manager. In our example also, the two positions labeled as Software Developer usually means a mid-level developer, who commonly have between 2 and 5 years experience. Senior developers will often have 5 to 8 or 10 years of experience. Again, the concept between these levels are similar in other areas of IT as well.
                </p>
                <p>
                  Also notice on this example team there are two others positions: QA Engineer and Test engineer. QA Engineers are Information Systems professionals who focus on code quality, defect detection and resolution and monitoring of the system from the perspective of the team they are assigned to, in this case, the User Interface Team.
                </p>
                <p>
                  Test Engineers are also Information Systems Professionals who specialize in developing testing procedures to code at all levels of development and deployment of the software system. They are often writing code for developing new tests and they often started their careers as programmers. Test engineering is a specialization within software development.
                </p>
                <p>
                  If you look back at the Software Development unit diagram, there are a couple of other positions you’ll see in the other two teams that you don’t see in team User Interface Team, described as follows:
                </p>
                <p>
                  Business Analyst is a position often held by people with business and/or management training who work as a liaison between their specific team and management and the business. When developers need answers to questions they have about the system requirements or development requests, they usually do not track down the answers from the various outside units who might be involved themselves. Instead they ask the business analysts. It is the analyst’s job to be very familiar with the needs of the business and what is needed in the software, or they know who to meet with to get the answers and then they assist the developer answer their questions.
                </p>
                <p>
                  UI/UX Engineers are usually not writing code (though some do), rather they are designers who create the user interface and provide the programmers with specifications for how the code behind the user interface should work. The programmers (developers) then write the code to give the UI/UX Engineer’s design its functionality.
                </p>
                <p>
                  In this article I’ve given a high-level overview of one possible configuration of an IT Department. As indicated, the structure of IT can vary a great deal from company to company. I also drilled down in to just one specific unit, Software Development, just to show an example. There are many many more details involved, our purpose here was to just give an overall idea how a department is structured. As you consider different areas of IT through your studies and job search, it will be important for you to explore this kind of information much more through your own research.
                </p>
